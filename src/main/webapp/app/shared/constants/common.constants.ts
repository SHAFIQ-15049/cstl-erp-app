export const YEARS = (begin: number, end: number) => {
  const leastYear = begin;
  const tillYear = end;
  const range: IConstant[] = [];
  for (let i = leastYear; i <= tillYear; i++) {
    const data: IConstant = {};
    data.id = i;
    data.name = i.toString();
    range.push(data);
  }
  return range;
};
